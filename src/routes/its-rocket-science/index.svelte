<BlogPost id="its-rocket-science">
  <p class="inspiration" slot="updates">
    Inspired by freecodecamp's great <a href="https://www.freecodecamp.org/news/scratch-has-a-marketing-problem-f84626bd18ef/">post about Scratch</a>
  </p>

  <p>
    How often have you told someone "<em>Don't worry, it's easy, you'll figure it out</em>"?
    How often have people said it to you?
    Do you remember what it felt like?
  </p>

  <p>
    I remember a few times, on both sides.
    On the receiving end, it feels discouraging and a bit belittling - like my fears aren't justified.
    Of course, <em>I</em> never mean it like that - just <em>everyone else</em> does.
    Right?
  </p>

  <h2>Actually Easy Things</h2>

  <p>
    Putting your shoes on is easy.
    Going to the shops is easy.
    Writing your name is easy.
  </p>

  <p>
    HTML is not easy.
    Deploying a lambda is not easy.
    FizzBuzz is actually pretty hard!
  </p>

  <p>
    Clearly, those things are <em>relatively</em> easy, compared to other more complex tasks.
    But they're only easy for <em>you</em> because you've <em>already</em> done all the hard work.
    You know what else isn't easy?
  </p>

  <ul>
    <li>Putting your shoes on is hard if you don't know how to tie your shoelaces.</li>
    <li>Going to the shops is hard if you can't walk, talk, read, or add up prices.</li>
    <li>Writing your name is hard if nobody taught you to write.</li>
  </ul>

  <p>
    It's <strong>contextual</strong>.
    You've been doing those things for so long, that you forgot how hard they all were at first.
    And when a child asks for help with their shoelaces, would you just tell them it's easy and point at your perfect bow?
    Of course not.
  </p>

  <h2>Why mention difficulty?</h2>

  <p>
    Why <em>do</em> we talk about the difficulty of a task?
    Why would we ever tell someone that a task is easy, or hard?
    What are we trying to achieve, and what <em>actually</em> happens?
  </p>

  <p>
    Usually, when we say a task is easy, we're trying to be encouraging.
    Maybe the learner is nervous, and scared of failing.
    It's probably true that the task <em>is</em> relatively easy.
    We're not trying to trick them by saying an impenetrable task is actually easy.
  </p>

  <p>
    On the other hand, we warn people about hard tasks so that they can get into the right frame of mind.
    Mistakes will happen, it'll take a long time to figure it out, and you shouldn't get discouraged when it goes wrong.
  </p>

  <p>
    We say "<em>it's easy</em>" to encourage someone to <strong>start</strong>, but say "<em>it's hard</em>" to encourage someone to <strong>finish</strong>.
    But let's be real here - <em>nothing</em> we do is easy.
    As coaches, mentors, or colleagues, we don't even <em>hear</em> about the things that are actually easy.
  </p>

  <p>
    Nobody's asking for help tying their shoes.
  </p>

  <h2>The Impact of Downplaying Difficulty</h2>

  <p>
    So when someone comes with a task that is hard <em>for them</em>, and we say it's actually easy, they become a <em>lot</em> more confident.
    They were afraid of messing up, but since now they <em>know</em> it's easy, they don't have to worry about failing.
  </p>

  <p>
    And then they mess it up.
  </p>

  <p>
    That would have been off-putting before, but now it's <strong>catastrophic</strong>.
    By saying the task was easy, you've removed any psychological safety that was there before.
    It's not a problem when you mess up a hard task - but what kind of idiot messes up on something so easy?
  </p>

  <p>
    It's pretty obvious in hindsight, but "<em>It's easy</em>" and "<em>It's hard</em>" are opposites.
    Saying a task is <em>easy</em> means more people will start, but failure hurts.
    If you claim a task is <em>hard</em>, then many won't even bother trying, but they'll stick with it through the failures.
  </p>

  <h2>A Better Way</h2>

  <p>
    If hard tasks put people off before starting, and easy tasks cause people to give up mid-way through, what's the alternative?
    How can we make failure expected and fun, without putting people off up front?
    Here's my best attempt:
  </p>

  <blockquote>
    <p>
      Learning anything is tricky, especially advanced stuff like this.
      So don't worry about making mistakes, because this <em>is</em> rocket science.
      Nothing explodes and nobody gets hurt when you mess up, so celebrate those chances to learn.
    </p>

    <p>
      Every time you do it wrong, try and figure out why, and how to prevent it.
      There's only so many ways it can break, so if you only make each mistake once it'll <em>have</em> to work eventually.
      Let me know when you get your first error, and we'll fix it together.
    </p>
  </blockquote>

  <p>
    Of course, you'll want to rephrase that - my wording is fine if you're mentoring students, but pretty patronising if you're helping a more senior colleague.
    Just make sure you hit all the important points:
  </p>

  <ul>
    <li>This <em>is</em> hard, and you will make mistakes</li>
    <li>Mistakes are <strong>harmless</strong></li>
    <li>Mistakes are <strong>learning</strong> opportunities</li>
    <li>Mistakes are <strong>progress</strong></li>
    <li>You <em>can</em> do this</li>
    <li>I'm here if you get stuck</li>
  </ul>

  <h2>Conclusion</h2>

  <p>
    It's instinctive to claim a task is easy, and un-learning that is <em>hard</em>.
    You <em>will</em> mess it up, so don't be too harsh on yourself, just follow up with support when they inevitably <em>do</em> make mistakes.
    Make a conscious effort to correct yourself, and it will get easier each time.
  </p>

  <p>
    You can do this, and I'm here if you get stuck.
  </p>
