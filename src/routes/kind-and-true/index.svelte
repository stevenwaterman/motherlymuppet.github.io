<BlogPost id="kind-and-true">

  <p>
    Before I say something, I try and ask myself three questions:
  </p>

  <ol>
    <li>Is it <strong>Kind</strong>?</li>
    <li>Is it <strong>True</strong>?</li>
    <li>Is it <strong>Necessary</strong>?</li>
  </ol>

  <p>
    I'd love to be able to give you a citation for where this originated, but I have no idea.
    GoodReads <a href="https://www.goodreads.com/quotes/823818-is-it-true-is-it-kind-or-is-it-necessary">attributes it</a> to Socrates, which is basically never true.
    It's debated, but seems like <a href="https://skeptics.stackexchange.com/questions/39843/are-socrates-3-tests-from-a-historical-source">it has religious origins</a>.
  </p>

  <p>
    Anyway, when it comes to those three questions, they don't all need to be answered with an enthusiastic yes.
    As a rule of thumb, you want 2 out of 3:
  </p>

  <ul>
    <li>It's ok to hurt someone's feelings with a necessary truth</li>
    <li>When necessary, a white lie is ok</li>
    <li><strong>You don't need a reason to share a kind truth</strong></li>
  </ul>

  <p>
    The last one's probably the most obvious, and hardest to argue against.
    But it's <em>also</em> the one that we do the least often.
    When was the last time you shared a kind truth with someone, just because you could?
  </p>

  <p>
    When was the last time you did it <em>at work</em>?
  </p>

  <h2>An Unnecessary Kind Truth</h2>

  <p>
    The world is a hostile and competitve place.
    When we share honest and unprompted praise, compliments, or appreciation, the world gets slightly nicer.
    It's a long journey, and this is just one step, but it's one step towards a world I'd rather live in.
  </p>

  <p>
    So take a moment to think to yourself.
    Think about the people that have helped you, the things you've read, the meetings you've enjoyed.
    Think about how people have helped make your life better, and repay the favour by thanking them.
  </p>

  <p>
    Tell a colleague <a href="https://youtu.be/Z8U1ekErATA?t=407">you're proud of them</a>, if you genuinely are.
  </p>

  <p>
    Say thanks to the bus driver, and throw a genuine compliment in with it.
  </p>

  <p>
    Send an appreciative email to your favourite school teacher.
  </p>

  <p>
    The thought of it probably makes you uncomfortable, and vulnerable.
    I'm right there with you, and it's not a nice feeling.
    How messed up is that - that in this world that we've created, we discourage sharing joy?
  </p>

  <p>
    I've pushed through that feeling, and it's always been a lovely experience.
    Nobody has reacted badly to being thanked, and why would they?
  </p>

  <h2>I Volunteer</h2>

  <p>
    Think about it from the other side.
    How would you feel, if someone spontaneously offered a sincere compliment about something you worked hard on?
    You probably <em>already</em> have an example in mind.
    It feels amazing, and you'll still smile when thinking about it, months or years later!
  </p>

  <p>
    We, as a society, <strong>are desperately starved for affection</strong>.
  </p>

  <p>
    There have been a few times in the past, when people have reached out and thanked me directly for a post on this blog.
    It's always lovely, and it brightens my day.
    If you want to practice, I'll gladly take your compliments!
    I'll even offer you my gratitude, and reassure you that it didn't feel weird on my end.
  </p>

  <p>
    So let's do this together.
    Let's take that first step towards a better world.
  </p>

  <p>
    Screw it, I noticed that my conclusion rhymed, so I turned it into a poem:
  </p>

  <blockquote><em>
    Let's agree together today<br>
    to share the thoughts that we can say<br>
    believing they're kind, believing they're true<br>
    whether or not we think we need to
  </em></blockquote>
