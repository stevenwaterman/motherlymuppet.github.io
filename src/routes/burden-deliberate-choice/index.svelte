<BlogPost id="burden-deliberate-choice">
  <p>
    I'd enjoy being a 15th-century shepherd.
    Not that I've got any particular skills that would make me good at it.
    And not that I think it would be an easy job.
    I think it would be pretty grueling.
  </p>

  <p>
    So why does it seem so appealing?
    So idyllic?
    So safe?
  </p>

  <p>
    I think it's the simplicity.
    The lack of choice, and the comfort I find in the meandering path of that life.
  </p>

  <p>
    I remember talking to a friend about how, in the last hundred years or so, the world has opened up immensely.
    A hundred years ago, your path in life was clear.
    Perhaps there were a few options, but the son of a poor coal miner never was never given the choice of becoming the town's dentist.
  </p>

  <p>
    It feels a bit heinous that I am, I suppose, grumpy at the choices in life that are afforded by social mobility...
  </p>

  <p>
    Frankly, that same social mobility means there's very little stopping me from completely pivoting my life onto a path towards shepherdhood.
    But it wouldn't be the same, because that in itself, is a choice.
    Waking up every morning and deciding to continue living that grueling life, <a href="https://www.youtube.com/watch?v=lkpLbEM06qE">is a choice</a>.
  </p>

  <p>
    It's not specifically about life paths and social mobility, though.
  </p>

  <p>
    <em>Everything</em> I interact with on a day-to-day basis is so complex that it feels fundamentally unknowable.
    I could dedicate my entire life to learning about the cable that connects this keyboard to my computer, or where the orange in my fridge came from, or the roof over my head.
  </p>

  <p>
    A lifetime is not enough to learn everything there is to know about any of the mundane objects or processes we interact with daily.
    Even crazier though, it's not even enough time to learn everything humanity <em>already</em> knows about them.
  </p>

  <p>
    The modern world is endlessly complex, and we have endlessly deep knowledge about endlessly broad topics.
    There are endless options, endless choices, endless opportunities that we decline without even realising.
  </p>

  <p>
    Right now, I am acutely aware of the constant deluge of opportunities I'm declining through inaction.
    And I <strong>hate it</strong>.
    I'm given the power to choose what I want, and therefore have to deal with the burden of choosing what I want, from an endless pool of unknowable options.
  </p>

  <p>
    I can choose to not choose.
    To act on impulse.
    But that too, is a choice.
    As with choosing to become a shepherd today, it would be a choice I have to make continuously for the rest of my life.
    Like an eternal trolley problem.
  </p>

  <p>
    I'd love to end with some kind of optimistic resolution that ties everything together, and resolves this inner tension.
    But it's unresolved.
  </p>

  <p>
    Which is why I'd enjoy being a 15th-century shepherd.
  </p>
</BlogPost>
