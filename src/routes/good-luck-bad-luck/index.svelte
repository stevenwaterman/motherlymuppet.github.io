<BlogPost id="good-luck-bad-luck">
	<p>
		I got hit by a car on Sunday. I'm mostly recovered, but my partner is still in a lot of pain.
		Nobody was seriously injured though.
	</p>

	<p>Was that bad luck? Good luck? Just a thing that happened?</p>

	<p>
		I'm taking it as good luck, and the same goes for <em>every</em> life event so far.
	</p>

	<p>Every accident. Every frustration. Every time I felt listless and depressed.</p>

	<p>
		That doesn't mean those things felt good at the time. But I wouldn't change it, if given the chance.
	</p>

	<p>
		If any of those things were different, I wouldn't be here, now, today, writing this. Life
		would be different. The butterfly effect would make sure of it.
	</p>

	<p>Different. Maybe worse, maybe better. But different.</p>

	<p>
		If we left the house half a second earlier, I might be dead. Two seconds earlier, I would've
		been <em>almost</em> hit by a car. An hour earlier? Maybe hit by a different car.
	</p>

	<p>
		Small changes earlier that day could've caused those seconds of delay. Tiny changes last year
		might have meant I wasn't even in the country.
	</p>

	<p>
		An <a href="https://en.wikipedia.org/wiki/The_old_man_lost_his_horse">ancient Chinese parable</a
		> talks about how we can never know whether an event is good or bad, without being able to predict
		its impact on the future.
	</p>

	<p>I wouldn't re-roll my life, if given the chance. So everything leading up to this point?</p>

	<p>Good luck.</p>

  <hr/>

  <p>
    I'm acutely aware of the fact that this reads like toxic positivity.
    And I certainly don't want to minimise any awful things going on in your life right now.
  </p>

  <p>
    You have every right to be sad, angry, and frustrated.
    Don't try and repress those emotions.
    It's ok to feel.
  </p>

  <p>
    I think I've just accepted that bad things are going to happen.
    But bad things have always happened.
    And my life still doesn't suck.
  </p>

  <p>
    So hopefully this will be the same.
  </p>
</BlogPost>
